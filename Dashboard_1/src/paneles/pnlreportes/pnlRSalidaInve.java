    private void btnGenerarSalidaIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarSalidaIActionPerformed
        try {
            Conexion con = new Conexion();

            Connection conn = con.getConnect();

            JasperReport reporte = null;
            String path = "src\\Reportes\\rSalidaInventario.jasper";

            Map parametro = new HashMap();
            
            String sucursal = SucursalSelected(cbSucursalSalidaI.getSelectedIndex());

            parametro.put("Sucursal", sucursal);

            Date fechaInicio = txtFechaInicio.getDate();
            Date fechaFinal = txtFechaFinal.getDate();

            if (fechaInicio != null && fechaFinal != null) {

                SimpleDateFormat dateFormatFechaInicio = new SimpleDateFormat("yyyy-MM-dd"); // Por ejemplo, "dd/MM/yyyy" o "yyyy-MM-dd"
                SimpleDateFormat dateFormatFechaFinal = new SimpleDateFormat("yyyy-MM-dd");

                String dateStringFechaInicio = dateFormatFechaInicio.format(fechaInicio);
                String dateStringFechaFinal = dateFormatFechaFinal.format(fechaFinal);

                String formatDateStringFechaInicio = "'" + dateStringFechaInicio + "'";
                String formatDateStringFechaFinal = "'" + dateStringFechaFinal + "'";

                parametro.put("FechaInicio", formatDateStringFechaInicio);
                parametro.put("FechaFinal", formatDateStringFechaFinal);
            } else {
                JOptionPane.showMessageDialog(null, "Se deben ingresar ambas fechas", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            reporte = (JasperReport) JRLoader.loadObjectFromFile(path);

            JasperPrint jprint = JasperFillManager.fillReport(reporte, parametro, conn);

            JasperViewer view = new JasperViewer(jprint, false);

            view.setDefaultCloseOperation(view.DISPOSE_ON_CLOSE);

            view.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(pnlRSalidaInve.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGenerarSalidaIActionPerformed

